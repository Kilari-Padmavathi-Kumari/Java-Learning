class Arithmetic
{
    public static void main(String[] args) {
        int a=10;
        int b=20;
        System.out.println("addition: " +(a+b));
        System.out.println("sub: "+ (a-b));
        System.out.println("mul: "+ (a*b));
        System.out.println("division: "+ (a/b));      // quotient
        System.out.println("module: "+ (a%b));         //remainder

    }
}